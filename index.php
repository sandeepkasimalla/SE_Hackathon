        <h2>Nutrition for good health</h2>
        <p style = "color:#ADD8E6">Good nutrition is one of the keys to a healthy life. You can improve your health by keeping a balanced diet. You should eat foods that contain vitamins and minerals. This includes fruits, vegetables, whole grains, dairy, and a source of protein.
Find the strong and weak points in your current diet. Do you eat 4-5 cups of fruits and vegetables every day? Do you get enough calcium? Do you eat whole grain, high-fiber foods? If so, you’re on the right track! Keep it up. If not, add more of these foods to your daily diet.
Rather than frying meat, bake, grill, or broil it. Take off the skin before cooking chicken or turkey. Try eating fish at least once a week.
Reduce any extra fat. 
This includes butter on bread, sour cream on baked potatoes, and salad dressings.
Use low-fat or nonfat versions of these foods.
Eat plenty of fruits and vegetables with your meals and as snacks.</p>


        <h2>Impact of yoga on health</h2>
        <h3>Decreases stress:</h3>
        <p style = "color:#ADD8E6">Yoga is known for its ability to ease stress and promote relaxation.In fact, multiple studies have shown that it can decrease the secretion of cortisol, the primary stress hormone</p>
        <h3>Relieves Anxiety:</h3>
        <p style = "color:#ADD8E6">Many people begin practicing yoga as a way to cope with feelings of anxiety.Interestingly enough, there is quite a bit of research showing that yoga can help reduce anxiety.</p>
        <h3>Fight Depression:</h3>
        <p style = "color:#ADD8E6">Some studies show that yoga may have an anti-depressant effect and could help decrease symptoms of depression.This may be because yoga is able to decrease levels of cortisol, a stress hormone that influences levels of serotonin, the neurotransmitter often associated with depression</p>
        <h3>Promote Sleep Quality:</h3>
        <p style = "color:#ADD8E6">Poor sleep quality has been associated with obesity, high blood pressure and depression, among other disorders. Yoga may help enhance sleep quality because of its effects on melatonin and its impact on several common contributors to sleep problems.</p>
